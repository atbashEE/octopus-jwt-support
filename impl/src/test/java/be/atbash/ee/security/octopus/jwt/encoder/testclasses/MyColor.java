package be.atbash.ee.security.octopus.jwt.encoder.testclasses;

public class MyColor {

    private int r;
    private int g;
    private int b;

    public MyColor(int r, int g, int b) {
        this.r = r;
        this.g = g;
        this.b = b;
    }

    public int getR() {
        return r;
    }

    public int getG() {
        return g;
    }

    public int getB() {
        return b;
    }
}
